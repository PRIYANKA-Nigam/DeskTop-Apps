package signup_form;

public class Modal {
	int id;
	String a,b;
	public Modal(int id, String a, String b) {
		super();
		this.id = id;
		this.a = a;
		this.b = b;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getA() {
		return a;
	}
	public void setA(String a) {
		this.a = a;
	}
	public String getB() {
		return b;
	}
	public void setB(String b) {
		this.b = b;
	}
	

}
